def greaterThan(x, y):
    if abs(x) > abs(y):
        print("True")
    else:
        print("False")
print(greaterThan(10, 8))
def lessThan(a, b):
    if abs(a) < abs(b):
        print("True")
    else:
        print("False")
print(lessThan(8, 8))
def equalTo(q, w):
    if abs(q) == abs(w):
        print("True")
    else:
        print("false")